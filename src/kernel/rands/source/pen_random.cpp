//  *********************************************************************
//                       FUNCTION RNDG3
//  *********************************************************************
double RNDG3(const CRNDG3& rndg3, pen_rand& rand)
{
  //  This function delivers random values in the interval (-3.0,3.0)
  //  sampled from a truncated Gaussian distribution that has zero mean and
  //  unit variance. The sampling is performed by using the RITA method.

  
  double RNDG3_RETURN;
  //  ****  Selection of the interval (Walker's aliasing).
  double RN = rand.rand()*rndg3.NPM1+1.0;
  int K = (int)RN;
  double TST = RN-(double)K;
  double RR, D;
  int I;
  if(TST < rndg3.F[K-1])
  {
    I = K;
    RR = TST;
    D = rndg3.F[K-1];
  }
  else
  {
    I = rndg3.KA[K-1];
    RR = TST-rndg3.F[K-1];
    D = 1.0-rndg3.F[K-1];
  }
  //  ****  Sampling from the rational inverse cumulative distribution.
  if(RR > 1.0E-12)
  {
    RNDG3_RETURN = rndg3.X[I-1]+((1.0+rndg3.A[I-1]+rndg3.B[I-1])*D*RR/(D*D+(rndg3.A[I-1]*D+rndg3.B[I-1]*RR)*RR))*(rndg3.X[I]-rndg3.X[I-1]);
  }
  else
  {
    RNDG3_RETURN = rndg3.X[I-1]+rand.rand()*(rndg3.X[I]-rndg3.X[I-1]);
  }
  
  return RNDG3_RETURN;
}

void RNDG30(CRNDG3& rndg3)
{
  //  Initialisation of the RNDG3 sampling function.


  CRITAA ritaa;
  
  int N = rndg3.NR;
  int NU = N/4;
  double XMIN = -3.0;
  double XMAX = +3.0;
  double ERRM;
  RITA0(RNDG3F,nullptr,XMIN,XMAX,N,NU,ERRM,0,ritaa);
  if(N != rndg3.NR){ penError(ERR_RNDG30_1); return;}
  if(ERRM > 1.0E-6){ penError(ERR_RNDG30_2); return;}

  for(unsigned int I = 0; I < rndg3.NR; I++)
  {
    rndg3.X[I] = ritaa.XA[I];
    rndg3.A[I] = ritaa.AA[I];
    rndg3.B[I] = ritaa.BA[I];
    rndg3.F[I] = ritaa.FA[I];
    rndg3.KA[I] = ritaa.IA[I];
    
  }
  rndg3.NPM1 = ritaa.NPM1A;
}

//  *********************************************************************
//                       SUBROUTINE RITA0
//  *********************************************************************
void RITA0(PDF_RITA PDF, void* arg, double XLOW, double XHIGH, int &N, int &NU, double &ERRM, int fileidx, CRITAA& ritaa)
{
  //  Initialisation of the RITA algorithm for random sampling of a
  //  continuous random variable X from a probability distribution function
  //  PDF(X) defined in the interval (XLOW,XHIGH). The external function
  //  PDF(X) --not necessarily normalised-- must be provided by the user.
  //  N is the number of points in the sampling grid. These points are
  //  determined by means of an adaptive strategy that minimizes local
  //  interpolation errors. The first NU grid points are uniformly spaced
  //  in (XLOW,XHIGH); when NU is negative, the initial grid consists of
  //  -NU points logarithmically spaced (in this case, XLOW must be
  //  non-negative).
  //
  //  ERRM is a measure of the interpolation error (the largest value of
  //  the absolute error of the rational interpolation integrated over each
  //  grid interval).
  //
  //  ****  Interpolation coefficients and PDF tables are printed on
  //        separate files (UNIT=IWR) if IWR is greater than zero.
  //
  //  Other subprograms needed: EXTERNAL function PDF,
  //                            subroutines RITAI0 and IRND0.
  //


  CRITA rita;
  
  //  ****  Initialisation of the RITA algorithm.
  RITAI0(PDF,arg,XLOW,XHIGH,N,NU,ERRM,fileidx,rita);
  //  ****  Walker's aliasing; cutoff and alias values.
  ritaa.NPM1A = rita.NPM1;
  for(int I = 0; I < rita.NPM1; I++)
  {
    ritaa.XA[I] = rita.XT[I];
    ritaa.AA[I] = rita.A[I];
    ritaa.BA[I] = rita.B[I];
  }
  ritaa.XA[rita.NPM1] = rita.XT[rita.NPM1];
  IRND0(rita.DPAC,ritaa.FA,ritaa.IA,ritaa.NPM1A);
  ritaa.FA[rita.NPM1] = 1.0;
  ritaa.IA[rita.NPM1] = ritaa.NPM1A;
}

//  *********************************************************************
//                       SUBROUTINE RITAI0
//  *********************************************************************
void RITAI0(PDF_RITA PDF, void* arg, double XLOW, double XHIGH, int N, int NU, double &ERRM, int fileidx, CRITA& rita)
{
  //  Initialisation of the RITA algorithm for random sampling of a
  //  continuous random variable X from a probability distribution function
  //  PDF(X) defined in the interval (XLOW,XHIGH). The external function
  //  PDF(X) --not necessarily normalised-- must be provided by the user.
  //  N is the number of points in the sampling grid. These points are
  //  determined by means of an adaptive strategy that minimizes local
  //  interpolation errors. The first NU grid points are uniformly spaced
  //  in (XLOW,XHIGH); when NU is negative, the initial grid consists of
  //  -NU points logarithmically spaced (in this case, XLOW must be
  //  nonnegative).
  //
  //  ERRM is a measure of the interpolation error (the largest value of
  //  the absolute error of the rational interpolation integrated over each
  //  grid interval).
  //
  //  ****  Interpolation coefficients and PDF tables are printed on
  //        separate files (UNIT=IWR) if IWR is greater than zero.
  //
  //  Other subprograms needed: EXTERNAL function PDF.
  //

  
  const double EPS = 1.0E-10;
  const double ZERO = 1.0E-75;
  const double ZEROT = 0.1*ZERO;

  //
  //  The information used by the sampling function RITAI is exported
  //  through the structure CRITA,

  //  where
  //    XT(I) ..... grid points, in increasing order.
  //    PAC(I) .... value of the cumulative pdf at XT(I).
  //    DPAC(I) ... probability of the I-th interval.
  //    A(I), B(I) ... rational inverse cumulative distribution parameters.
  //    IL(I) .... largest J for which PAC(J) < (I-1)/(NP-1).
  //    IU(I) .... smallest J for which PAC(J) > I/(NP-1).
  //    NPM1 ..... numner of grid points minus one, NP-1 (8.LE.NP.LE.NM).

  //    CNORM .... normalising constant of the external PDF, output.
  //

  //Take struct pointers
  double* XT   = rita.XT;
  double* DPAC = rita.DPAC;
  double* A    = rita.A;
  double* B    = rita.B;
  double* PAC  = rita.PAC;

  int* IL   = rita.IL;
  int* IU   = rita.IU;
  
  double ERR[rita.NM], C[rita.NM];
  const int NIP = 51;
  double XS[NIP], YS[NIP], SUMI[NIP];
  int NUNIF, NP_RITA;
  char Dirname[]="RITAI0";

  static int naccess=0;
  if(fileidx>0){naccess++;}
  //if(fileidx>0 && naccess==1)
  //{
    //char Command[100];
    //strcpy(Command,"mkdir -p ");
    //strcat(Command,Dirname);
    //system(Command);   
  //}

  if(N < 9)
  {
    printf(" Error in RITAI0: N must be larger than 8.\n N=%11d\n", N);
    penError(ERR_RITAI0_N_8); return;
  }
  if(N > (int)rita.NM)
  {
    printf(" Error in RITAI0: N must be less than NM=%d.\n N=%11d\n", rita.NM, N);
    penError(ERR_RITAI0_N_NM); return;
  }
  if(XLOW > XHIGH-EPS)
  {
    printf(" Error in RITAI0: XLOW must be larger than XHIGH.\n XLOW=%13.6E, XHIGH =%13.6E\n", XLOW, XHIGH);
    penError(ERR_RITAI0_XLOW_XHIGH); return;
  }

  //  ****  We start with a grid of NUNIF points uniformly spaced in the
  //        interval (XLOW,XHIGH).

  if(NU >= 0)
  {
    NUNIF = (NU > 8 ? NU : 8);
    if(NUNIF > N/2){ NUNIF = N/2;}
      
    NP_RITA = NUNIF;
    double DX = (XHIGH-XLOW)/double(NP_RITA-1);
    XT[0] = XLOW;
    for(int I = 0; I < NP_RITA-1; I++)
    {
      XT[I+1] = XLOW+(I+1)*DX;     
    }
    XT[NP_RITA-1] = XHIGH;
  }
  else
  {
      //  ****  If NU.LT.0,the NUNIF points are logarithmically spaced.
      //        XLOW must be greater than or equal to zero.
    NUNIF = (-NU > 8 ? -NU : 8);
    if(NUNIF > N/2){ NUNIF = N/2;}
      
    NP_RITA = NUNIF;
    if(XLOW < 0.0)
    {
      printf(" Error in RITAI0: XLOW and NU are negative.\n XLOW=%14.7E,  NU=%11d\n", XLOW, NU);
      penError(ERR_RITAI0_XLOW_NU_NEG); return;
    }
    XT[0] = XLOW;
    int I1;
    if(XLOW < 1.0E-16)
    {
      XT[1] = XLOW+1.0E-6*(XHIGH-XLOW);
      I1 = 2;
    }
    else
    {
      I1 = 1;
    }
    double FX = exp(log(XHIGH/XT[I1-1])/double(NP_RITA-I1));
    for(int I = I1-1; I < NP_RITA-1; I++)
    {
      XT[I+1] = XT[I]*FX;
    }
    XT[NP_RITA-1] = XHIGH;
  }

  for(int I = 0; I < NP_RITA-1; I++)
  {
    double DX = XT[I+1]-XT[I];
    double DXI = DX/double(NIP-1);
    double PDFMAX = 0.0;
    for(int K = 0; K < NIP; K++)
    {
      XS[K] = XT[I]+double(K+1-1)*DXI;

      YS[K] = PDF(XS[K],arg);
      if(YS[K] < ZEROT){ YS[K] = ZEROT;}

      if(PDFMAX < YS[K]){ PDFMAX = YS[K];}
    
    }
      //  ****  Simpson's integration.
    double CONS = DXI*3.3333333333333333E-1*0.5;
    SUMI[0] = 0.0;
    for(int K = 1; K < NIP; K++)
    {
      double XIH = XS[K]-0.5*DXI;

      double YSH = PDF(XIH,arg);
      if(YSH < ZEROT){ YSH = ZEROT;}

      if(PDFMAX < YSH){ PDFMAX = YSH;}

      SUMI[K] = SUMI[K-1]+CONS*(YS[K-1]+4.0*YSH+YS[K]);
    }

    DPAC[I] = SUMI[NIP-1];
    double FACT = 1.0/DPAC[I];
    for(int K = 0; K < NIP; K++)
    {
      SUMI[K] = FACT*SUMI[K];
    }
      //  ****  When the PDF vanishes at one of the interval end points, its
      //        value is modified.
    if(YS[0] < ZERO){ YS[0] = 1.0E-5*PDFMAX;}
    if(YS[NIP-1] < ZERO){ YS[NIP-1] = 1.0E-5*PDFMAX;}

    double PLI = YS[0]*FACT;
    double PUI = YS[NIP-1]*FACT;
    B[I] = 1.0-1.0/(PLI*PUI*DX*DX);
    A[I] = (1.0/(PLI*DX))-1.0-B[I];
    C[I] = 1.0+A[I]+B[I];
    if(C[I] < ZERO)
    {
      A[I] = 0.0;
      B[I] = 0.0;
      C[I] = 1.0;
    }

      //  ****  ERR(I) is the integral of the absolute difference between the
      //  rational interpolation and the true PDF, extended over the interval
      //  (XT(I),XT(I+1)). Calculated using the trapezoidal rule.

    int ICASE = 1;
    bool Eixir = false;
    while(!Eixir)
    {
      Eixir = true;
      ERR[I] = 0.0;
      for(int K = 0; K < NIP; K++)
      {
        double RR = SUMI[K];
        double PAP = DPAC[I]*pow(1.0+(A[I]+B[I]*RR)*RR,2)/((1.0-B[I]*RR*RR)*C[I]*(XT[I+1]-XT[I]));
        if(K+1 == 1 || K+1 == NIP)
        {
          ERR[I] = ERR[I]+0.5*fabs(PAP-YS[K]);
        }
        else
        {
          ERR[I] = ERR[I]+fabs(PAP-YS[K]);
        }
      }
      ERR[I] = ERR[I]*DXI;
    //  ****  If ERR(I) is too large, the PDF is approximated by a uniform
    //        distribution.
      if(ERR[I] > 0.10*DPAC[I] && ICASE == 1)
      {
        B[I] = 0.0;
        A[I] = 0.0;
        C[I] = 1.0;
        ICASE = 2;
        Eixir = false;
        continue;
      }
    }
  }
  XT[NP_RITA-1] = XHIGH;
  A[NP_RITA-1] = 0.0;
  B[NP_RITA-1] = 0.0;
  C[NP_RITA-1] = 0.0;
  ERR[NP_RITA-1] = 0.0;
  DPAC[NP_RITA-1] = 0.0;

  //  ****  New grid points are added by halving the subinterval with the
  //        largest absolute error.

  bool Eixir = false;
  while(!Eixir)
  {
    Eixir = true;
    ERRM = 0.0;
    int LMAX = 1;
    for(int I = 0; I < NP_RITA-1; I++)
    {
    //  ****  ERRM is the largest of the interval errors ERR(I).
      if(ERR[I] > ERRM)
      {
        ERRM = ERR[I];
        LMAX = I+1;
      }
    }
      
    NP_RITA = NP_RITA+1;
    for(int I = NP_RITA-1; I >= LMAX+1-1; I--)
    {
      XT[I] = XT[I-1];
      A[I] = A[I-1];
      B[I] = B[I-1];
      C[I] = C[I-1];
      ERR[I] = ERR[I-1];
      DPAC[I] = DPAC[I-1];
    }
    XT[LMAX+1-1] = 0.5*(XT[LMAX-1]+XT[LMAX+2-1]);
    for(int I = LMAX-1; I < LMAX+1; I++)
    {
      double DX = XT[I+1]-XT[I];
      double DXI = (XT[I+1]-XT[I])/double(NIP-1);
      double PDFMAX = 0.0;
      for(int K = 0; K < NIP; K++)
      {
        XS[K] = XT[I]+double(K+1-1)*DXI;
        YS[K] = PDF(XS[K],arg);
        if(YS[K] < ZEROT){ YS[K] = ZEROT;}
        if(PDFMAX < YS[K]){ PDFMAX = YS[K];}
      }
    //  ****  Simpson's integration.
      double CONS = DXI*3.3333333333333333E-1*0.5;
      SUMI[0] = 0.0;
      for(int K = 1; K < NIP; K++)
      {
        double XIH = XS[K]-0.5*DXI;      
        double YSH = PDF(XIH,arg);
        if(YSH < ZEROT){ YSH = ZEROT;}  
        SUMI[K] = SUMI[K-1]+CONS*(YS[K-1]+4.0*YSH+YS[K]);
      }

      DPAC[I] = SUMI[NIP-1];
      double FACT = 1.0/DPAC[I];
      for(int K = 0; K < NIP; K++)
      {
        SUMI[K] = FACT*SUMI[K];
      }

      if(YS[0] < ZERO){ YS[0] = 1.0E-5*PDFMAX;}
      if(YS[NIP-1] < ZERO){ YS[NIP-1] = 1.0E-5*PDFMAX;}
      double PLI = YS[0]*FACT;
      double PUI = YS[NIP-1]*FACT;
      B[I] = 1.0-1.0/(PLI*PUI*DX*DX);
      A[I] = (1.0/(PLI*DX))-1.0-B[I];
      C[I] = 1.0+A[I]+B[I];
      if(C[I] < ZERO)
      {
        A[I] = 0.0;
        B[I] = 0.0;
        C[I] = 1.0;
      }

      int ICASE = 1;
      bool Eixir2 = false;
      while(!Eixir2)
      {
        Eixir2 = true;
        ERR[I] = 0.0;
        for(int K = 0; K < NIP; K++)
        {
          double RR = SUMI[K];
          double PAP = DPAC[I]*pow(1.0+(A[I]+B[I]*RR)*RR,2)/((1.0-B[I]*RR*RR)*C[I]*(XT[I+1]-XT[I]));
          if(K+1 == 1 || K+1 == NIP)
          {
            ERR[I] = ERR[I]+0.5*fabs(PAP-YS[K]);
          }
          else
          {
            ERR[I] = ERR[I]+fabs(PAP-YS[K]);
          }
        }
        ERR[I] = ERR[I]*DXI;

        if(ERR[I] > 0.10*DPAC[I] && ICASE == 1)
        {
          B[I] = 0.0;
          A[I] = 0.0;
          C[I] = 1.0;
          ICASE = 2;
          Eixir2 = false;
          continue;
        }
      }
    }

    if(NP_RITA < N){ Eixir = false; continue;}
  }
  rita.NPM1 = NP_RITA-1;

  //  ****  Renormalisation.

  double CNORM = 0.0;
  for(int I = 0; I < rita.NPM1; I++)
  {
    CNORM = CNORM+DPAC[I];
  }
  CNORM = 1.0/CNORM;
  ERRM = 0.0;
  for(int I = 0; I < rita.NPM1; I++)
  {
    DPAC[I] = DPAC[I]*CNORM;
    ERR[I] = ERR[I]*CNORM;

    if(ERRM < ERR[I]){ ERRM = ERR[I];}
      
  }

  PAC[0] = 0.0;
  for(int I = 0; I < rita.NPM1; I++)
  {
      PAC[I+1] = PAC[I]+DPAC[I];
  }
  PAC[NP_RITA-1] = 1.0;

  //  ****  Pre-calculated limits for the initial binary search in
  //        subroutine RITAI.

  double BIN = 1.0/double(rita.NPM1);
  IL[0] = 1;
  for(int I = 1; I < rita.NPM1; I++)
  {
    double PTST = (I+1-1)*BIN;
    for(int J = IL[I-1]-1; J < NP_RITA; J++)
    {
      if(PAC[J] > PTST)
      {
        IL[I] = J+1-1;
        IU[I-1] = J+1;
        break;
      }
    }
  }
  IU[rita.NPM1-1] = NP_RITA;
  IL[NP_RITA-1] = NP_RITA-1;
  IU[NP_RITA-1] = NP_RITA;

  //  ****  Print interpolation tables (only when IWR.GT.0).

  if(fileidx > 0)
  {
    FILE* IWR;
    char Buffer[150];
    sprintf(Buffer,"%s%s%s%d%s",Dirname,"/","param",naccess,".dat");
    IWR = fopen(Buffer, "w");
    fprintf(IWR, " #  Interpolation error =%15.7E\n", ERRM);
    fprintf(IWR, " # Normalising constant =%15.7E\n", CNORM);
    fprintf(IWR, " #     X           PDF(X)          A             B             C           error\n");
    for(int I = 0; I < rita.NPM1; I++)
    {
      double PDFE = PDF(XT[I],arg);
      if(PDFE < ZEROT){ PDFE = ZEROT;}

      PDFE = PDFE*CNORM;

      fprintf(IWR, "%14.6E%14.6E%14.6E%14.6E%14.6E%14.6E\n", XT[I], PDFE, A[I], B[I], C[I], ERR[I]);
    }
    fclose(IWR);

    sprintf(Buffer,"%s%s%s%d%s",Dirname,"/","table",naccess,".dat");
    IWR = fopen(Buffer, "w");
    fprintf(IWR, " #  Interpolation error =%15.7E\n", ERRM);
    fprintf(IWR, " # Normalising constant =%15.7E\n", CNORM);
    fprintf(IWR, " #      X             PDF_ex          PDF_ap           err\n");

    for(int I = 0; I < rita.NPM1; I++)
    {
      double DX = (XT[I+1]-XT[I])/double(NIP-1);
      for(int K = 0; K < NIP; K += 5)
      {
        double XTAU = XT[I]+double(K+1-1)*DX;

        double P1 = PDF(XTAU,arg)*CNORM;
        if(P1 < ZEROT){ P1 = ZEROT;}
        
        //  ****  Rational interpolation.
        double TAU = (XTAU-XT[I])/(XT[I+1]-XT[I]);
        double CON1 = 2.0*B[I]*TAU;
        double CON2 = C[I]-A[I]*TAU;
        double ETA;
        if(fabs(CON1) > 1.0E-10*fabs(CON2))
        {
          ETA = CON2*(1.0-sqrt(1.0-2.0*TAU*CON1/pow(CON2,2)))/CON1;
        }
        else
        {
          ETA = TAU/CON2;
        }
        double P2 = DPAC[I]*pow(1.0+(A[I]+B[I]*ETA)*ETA,2)/((1.0-B[I]*ETA*ETA)*C[I]*(XT[I+1]-XT[I]));
        fprintf(IWR, " %15.8E %15.8E %15.8E %15.8E\n", XTAU, P1, P2, (P1-P2)/P1);
      }
    }
    fclose(IWR);

    sprintf(Buffer,"%s%s%s%d%s",Dirname,"/","limits",naccess,".dat");
    IWR = fopen(Buffer, "w");    
    fprintf(IWR, " #  I      PAC(ITL)         (I-1)/NPM1           I/NPM1            PAC(ITU)            PAC(I)\n");
    for(int I = 0; I < rita.NPM1; I++)
    {
      fprintf(IWR, "%5d %18.11E %18.11E %18.11E %18.11E %18.11E\n", I+1, PAC[IL[I]-1], (I+1-1)*BIN, (I+1)*BIN, PAC[IU[I]-1], PAC[I]);
      if(PAC[IL[I]-1] > (I+1-1)*BIN+EPS || PAC[IU[I]-1] < (I+1)*BIN-EPS)
      {
        fprintf(IWR, " #  WARNING: The first four values should be in increasing order.\n");
      }
    }
    fclose(IWR);
  }
}

//  *********************************************************************
//                       FUNCTION IRND
//  *********************************************************************
int IRND(double* FA, int* IA, int &N, pen_rand& random)
{
  //  Random sampling from a discrete probability distribution using
  //  Walker's aliasing algorithm.
  //
  //  The arrays F and IA are determined by the initialisation routine
  //  IRND0, which must be invoked before using function IRND.
  //
  //  Input arguments:
  //    FA(1:N) ... cutoff values.
  //    IA(1:N) ... alias values.
  //    N ......... number of different values of the random variable.
  //
  //  Output argument:
  //    IRND ...... sampled value.
  //
  //  Other subprograms needed: function RAND and subroutine IRND0.
  //

  double RN = random.rand()*N;
  int IRND_RETURN = int(RN);
  double TST = RN-IRND_RETURN;
  if(TST > FA[IRND_RETURN-1]){ IRND_RETURN = IA[IRND_RETURN-1];}

  return IRND_RETURN;
}

//  *********************************************************************
//                       SUBROUTINE IRND0
//  *********************************************************************
void IRND0(double* W, double* F, int* K, int &N)
{
  //  Initialisation of Walker's aliasing algorithm for random sampling
  //  from discrete probability distributions.
  //
  //  Input arguments:
  //    N ........ number of different values of the random variable.
  //    W(1:N) ... corresponding point probabilities (not necessarily
  //               normalised to unity).
  //  Output arguments:
  //    F(1:N) ... cutoff values.
  //    K(1:N) ... alias values.


  //  ****  Renormalisation.
  double CNORM = 0.0;
  for(int I = 0; I < N; I++)
  {
    if(W[I] > 0.0){ CNORM = CNORM+W[I];}   
  }
  CNORM = 1.0/CNORM;
  double FACT = double(N)*CNORM;
  for(int I = 0; I < N; I++)
  {
    K[I] = I+1;
    F[I]=0.0;
    if(W[I] > 0.0){F[I] = W[I]*FACT;};
  }
  if(N == 1){return;}
  //  ****  Cutoff and alias values.
  for(int I = 0; I < N-1; I++)
  {
    double HLOW = 1.0;
    double HIGH = 1.0;
    int ILOW = 0;
    int IHIGH = 0;
    for(int J = 0; J < N; J++)
    {
      if(K[J] == J+1)
      {
        if(F[J] < HLOW)
        {
          HLOW = F[J];
          ILOW = J+1;
        }
        else if(F[J] > HIGH)
        {
          HIGH = F[J];
          IHIGH = J+1;
        }
      }
    }
    if(ILOW == 0 || IHIGH == 0){ return;}
    K[ILOW-1] = IHIGH;
    F[IHIGH-1] = HIGH+HLOW-1.0;
  }
}

//  *********************************************************************
//                       SUBROUTINE RITAM
//  *********************************************************************
void RITAM(double XD, double XU, double &XM0, double &XM1, double &XM2, CRITA& rita)
{
  //  Calculation of (restricted) momenta of a pdf, PDF(X), obtained from
  //  its RITA approximation.
  //
  //     XD, XU ... limits of the integration interval.
  //     XM0 ...... 0th order moment.
  //     XM1 ...... 1st order moment.
  //     XM2 ...... 2nd order moment.


  const int NIP = 51;
  double XS[NIP], YS[NIP];

  XM0 = 0.0;
  XM1 = 0.0;
  XM2 = 0.0;
  for(int I = 0; I < rita.NPM1; I++)
  {
    if(rita.XT[I+1] >= XD && rita.XT[I] <= XU)
    {
      double X1 = (rita.XT[I] > XD ? rita.XT[I] : XD);
      double X2 = (rita.XT[I+1] < XU ? rita.XT[I+1] : XU);
      double DX = (X2-X1)/double(NIP-1);

      for(int K = 0; K < NIP; K++)
      {
        XS[K] = X1+double(K+1-1)*DX;
        //  ****  Value of the RITA rational pdf at the point XS(K).
        double TAU = (XS[K]-rita.XT[I])/(rita.XT[I+1]-rita.XT[I]);
        double CON1 = 2.0*rita.B[I]*TAU;
        double CON2 = 1.0+rita.B[I]+rita.A[I]*(1.0-TAU);
        double ETA;
        if(fabs(CON1) > 1.0E-10*fabs(CON2))
        {
          ETA = CON2*(1.0-sqrt(1.0-2.0*TAU*CON1/pow(CON2,2)))/CON1;
        }
        else
        {
          ETA = TAU/CON2;
        }
        YS[K] = rita.DPAC[I]*pow(1.0+(rita.A[I]+rita.B[I]*ETA)*ETA,2)/((1.0-rita.B[I]*ETA*ETA)*(1.0+rita.A[I]+rita.B[I])*(rita.XT[I+1]-rita.XT[I]));
      }

    //  ****  Simpson's integration.

      double CONS = DX*3.3333333333333333E-1;
      double SUM = 0.0;
      for(int L = 2; L < NIP; L += 2)
      {
        SUM = SUM+YS[L-2]+4.0*YS[L-1]+YS[L];
      }
      XM0 = XM0+SUM*CONS;

      for(int K = 0; K < NIP; K++)
      {
        YS[K] = YS[K]*XS[K];
      }
      SUM = 0.0;
      for(int L = 2; L < NIP; L +=2)
      {
        SUM = SUM+YS[L-2]+4.0*YS[L-1]+YS[L];
      }
      XM1 = XM1+SUM*CONS;

      for(int K = 0; K < NIP; K++)
      {
        YS[K] = YS[K]*XS[K];
      }
      SUM = 0.0;
      for(int L = 2; L < NIP; L += 2)
      {
        SUM = SUM+YS[L-2]+4.0*YS[L-1]+YS[L];
      }
      XM2 = XM2+SUM*CONS;
    }
  }     
}

//  *********************************************************************
double RNDG3F(double X, void* /*arg*/)
{
  //  Truncated Gaussian distribution, restricted to the interval (-3.0,
  //  3.0), with zero mean and unit variance.

  double RNDG3F_RETURN;
  if(fabs(X) > 3.00001)
  {
    RNDG3F_RETURN = 0.0;
  }
  else
  {
    RNDG3F_RETURN = exp(-X*X*0.5/pow(1.01538698,2));
  }
  return RNDG3F_RETURN;
}
